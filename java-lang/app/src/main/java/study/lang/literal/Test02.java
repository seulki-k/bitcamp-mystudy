package study.lang.literal;

// 실습

// -10진수 리터럴 100을 지시에 따라 다양한 진수 리터럴로 표현
// 출력
// 100
// 100
// 100
// 100
// 100

public class Test02 {
    public static void main(String[] args) {

        System.out.println(100); // 10진수
        System.out.println(0b1010); // 2진수
        System.out.println(012); // 8진수
        System.out.println(0xA); // 16진수
    }
}
